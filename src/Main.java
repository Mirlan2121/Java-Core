public class Main {
    public static void main(String[] args) {
        //В Java типы данных делят на две большие группы: примитивные и ссылочные.
        // В состав примитивных типов (или просто примитивов) входят
        // четыре подвида и восемь типов данных:

        //Целые числа Java (byte, short, int, long);
        int num = 123456789;
        long numL = 1234567890;
        byte numB = 123;
        short numS = 12345;

        //Числа с плавающей точкой (float, double);
        double num1 = 1.23456789;
        float num2= 1.234567f;

        //Логический (boolean);
        boolean bool = true;
        boolean bool1 = false;

        //Символьный (char).
        char ch = 'c';

        System.out.println();
    }
}
